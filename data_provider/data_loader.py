"""Этот код содержит определения нескольких классов на Python, используемых для создания датасетов, которые подготавливают данные для последующего использования в машинном обучении, в особенности для временных рядов. Каждый из классов наследует Dataset из библиотеки torch.utils.data, что является стандартным подходом для работы с данными в PyTorch.

Класс Dataset_ETT_hour
Этот класс предназначен для работы с часовыми временными рядами. Он включает следующие ключевые функциональные элементы:

Инициализация: В методе __init__ происходит настройка параметров, таких как длина последовательности (seq_len), длина меток (label_len), и длина предсказания (pred_len). Также определяется, являются ли данные обучающими, тестовыми или валидационными.
Чтение данных: Метод __read_data__ читает данные из CSV-файла, масштабирует их с помощью StandardScaler, и генерирует временные метки. Эти данные разделяются на тренировочные, валидационные и тестовые наборы.
Получение элемента: Метод __getitem__ извлекает и возвращает наборы данных (признаки и цели) для заданного индекса.
Возврат длины: Метод __len__ возвращает количество доступных примеров для итерации.
Класс Dataset_ETT_minute
Аналогичен классу Dataset_ETT_hour, но предназначен для работы с минутными временными рядами. Основное отличие заключается в обработке временных меток, которые включают информацию о минутах.

Класс Dataset_Custom
Этот класс позволяет работать с настраиваемым датасетом. Он аналогичен предыдущим классам, но предполагает возможность более гибкой работы с данными и масштабирования.

Класс Dataset_Pred
Этот класс предназначен для предсказаний на основе временных рядов. Особенностью является возможность работы с данными в реальном времени для генерации предсказаний.

Общие элементы
Во всех классах используются:

Масштабирование данных: Применяется StandardScaler для нормализации данных перед обработкой.
Временные метки: Обрабатываются даты и времена для генерации дополнительных признаков, таких как месяц, день, день недели и час.
Метод inverse_transform: Преобразует масштабированные данные обратно в исходное состояние, что полезно для интерпретации результатов модели.
Этот код полезен для создания датасетов для обучения и тестирования моделей прогнозирования временных рядов в PyTorch, обеспечивая эффективное управление данными и подготовку к обучению модели."""

class Dataset_ETT_hour(Dataset):
    def __init__(self, root_path, flag='train', size=None,
                 features='S', data_path='ETTh1.csv',
                 target='OT', scale=True, timeenc=0, freq='h'):
        # size [seq_len, label_len, pred_len]
        # info
        if size == None:
            self.seq_len = 24 * 4 * 4
            self.label_len = 24 * 4
            self.pred_len = 24 * 4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]
        # init
        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]

        self.features = features
        self.target = target
        self.scale = scale
        self.timeenc = timeenc
        self.freq = freq

        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        self.scaler = StandardScaler()
        df_raw = pd.read_csv(os.path.join(self.root_path,
                                          self.data_path))
        """В данном фрагменте кода определяются границы для разделения временного ряда на тренировочные, валидационные и тестовые подмножества данных. Разделение происходит на основе индексов, которые представляют собой количество часов. Давайте подробнее рассмотрим, что значат эти числа и как они используются:

        Объявление границ:
        border1s - массив, содержащий начальные индексы для тренировочного, валидационного и тестового наборов данных.

        0: Начало тренировочного набора.
        12 * 30 * 24 - self.seq_len: Начало валидационного набора, которое начинается после первого года данных (12 месяцев по 30 дней, каждый день по 24 часа) с учетом длины входной последовательности seq_len, чтобы обеспечить достаточно данных для создания последней тренировочной выборки.
        12 * 30 * 24 + 4 * 30 * 24 - self.seq_len: Начало тестового набора, которое начинается после 16 месяцев данных (после 12 месяцев тренировочных данных и 4 месяцев валидационных данных), также с учетом seq_len.
        border2s - массив, содержащий конечные индексы для каждого из наборов данных.

        12 * 30 * 24: Конец тренировочного набора, который соответствует одному году.
        12 * 30 * 24 + 4 * 30 * 24: Конец валидационного набора, который соответствует 16 месяцам данных.
        12 * 30 * 24 + 8 * 30 * 24: Конец тестового набора, который соответствует 20 месяцам данных.
        Применение границ:
        border1 и border2: Определяются на основе self.set_type, который указывает, к какому типу набора данных (тренировочный, валидационный, тестовый) принадлежит текущий экземпляр класса.
        Если set_type равен 0 (тренировочный набор), то используются первые элементы из border1s и border2s.
        Если set_type равен 1 (валидационный набор), то используются вторые элементы.
        Если set_type равен 2 (тестовый набор), то используются третьи элементы.
        Этот механизм позволяет автоматически выделить разные сегменты данных для различных этапов обучения и тестирования модели, обеспечивая корректное разделение и предотвращая утечку данных между наборами."""
        border1s = [0, 12 * 30 * 24 - self.seq_len, 12 * 30 * 24 + 4 * 30 * 24 - self.seq_len]
        border2s = [12 * 30 * 24, 12 * 30 * 24 + 4 * 30 * 24, 12 * 30 * 24 + 8 * 30 * 24]
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]

        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns[1:]
            df_data = df_raw[cols_data]
        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        if self.scale:
            train_data = df_data[border1s[0]:border2s[0]]
            self.scaler.fit(train_data.values)
            data = self.scaler.transform(df_data.values)
        else:
            data = df_data.values

        df_stamp = df_raw[['date']][border1:border2]
        df_stamp['date'] = pd.to_datetime(df_stamp.date)
        if self.timeenc == 0:
            df_stamp['month'] = df_stamp.date.apply(lambda row: row.month, 1)
            df_stamp['day'] = df_stamp.date.apply(lambda row: row.day, 1)
            df_stamp['weekday'] = df_stamp.date.apply(lambda row: row.weekday(), 1)
            df_stamp['hour'] = df_stamp.date.apply(lambda row: row.hour, 1)
            data_stamp = df_stamp.drop(['date'], axis=1).values
        elif self.timeenc == 1:
            data_stamp = time_features(pd.to_datetime(df_stamp['date'].values), freq=self.freq)
            data_stamp = data_stamp.transpose(1, 0)


        self.data_x = data[border1:border2]
        self.data_y = data[border1:border2]
        self.data_stamp = data_stamp

    def __getitem__(self, index):
        s_begin = index
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        seq_x = self.data_x[s_begin:s_end]
        seq_y = self.data_y[r_begin:r_end]
        seq_x_mark = self.data_stamp[s_begin:s_end]
        seq_y_mark = self.data_stamp[r_begin:r_end]

        return seq_x, seq_y, seq_x_mark, seq_y_mark

    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)


